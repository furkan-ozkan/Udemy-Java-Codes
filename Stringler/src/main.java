
public class main {

	public static void main(String[] args) {
		
		String s = "Furkan Ozkan Temel Duzey Java Egitim Seti";
		// charAt();
		System.out.println(s.charAt(1));
		// indexOf();
		System.out.println(s.indexOf("r"));
		// toUpperCase();
		System.out.println(s.toUpperCase());
		// toLowerCase();
		System.out.println(s.toLowerCase());
		// length();
		System.out.println(s.length());
		// concat();
		System.out.println(s.concat("(Baslangýç seviyesi)"));
		// replace();
		System.out.println(s.replace('a', 'e'));
		// /n
		System.out.println("Furkan \nOzkan");
		// /t
		System.out.println("Furkan \tOzkan");
	}

}
